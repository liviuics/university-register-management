    private void jPasswordField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) { //se verifica daca tasta aasata este Enter
            PreparedStatement ps;
        ResultSet rs;
        String username=jTextField1.getText();
        String password=String.valueOf(jPasswordField1.getPassword());
        String query="SELECT * FROM `admin` WHERE `Username`=? AND `Password`=?";
        try {
            ps=MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);
            rs=ps.executeQuery();
            if(rs.next()){
                if(username.equals("admin")){
                    JOptionPane.showMessageDialog(null,"V-ati logat drept admin");
                    AdminMainForm2 amf=new AdminMainForm2();
                    amf.setVisible(true);
                    amf.pack();
                    amf.setLocationRelativeTo(null);
                    amf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null,"V-ati logat cu succes");
                    AdminMainForm2 mf=new AdminMainForm2();
                    mf.setVisible(true);
                    mf.pack();
                    mf.setLocationRelativeTo(null);
                    mf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null,"Ati introdus date gresite!\n Daca nu aveti cont va rugam inregistrati-va");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }     
   }
    }//GEN-LAST:event_jPasswordField1KeyPressed

