    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        String username=jTextField1.getText(); //se ia textul din campul text corespunzator username
        String password=String.valueOf(jPasswordField1.getPassword()); //se ia textul din campul text corespunzator parolei
        String query="SELECT * FROM ics.admin WHERE Username=? AND Password=?"; //interogarea sql
        try {
            ps=MyConnection.getConnection().prepareStatement(query); //se pregateste interogarea
            ps.setString(1, username); // se pregateste campul 1 din tabelul din baze de date, si anume username
            ps.setString(2, password); // se pregateste campul 2 din tabelul din baze de date, si anume password, campul 0 este ID
            rs=ps.executeQuery(); //se executa interogarea
            if(rs.next()){ //daca interogarea este corecta
                //logare drept admin
                if(username.equals("admin")){
                    JOptionPane.showMessageDialog(null,"V-ati logat drept admin");
                    AdminMainForm2 amf=new AdminMainForm2();
                    amf.setVisible(true);
                    amf.pack();
                    amf.setLocationRelativeTo(null);
                    amf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();                    
                }
            }else{
                JOptionPane.showMessageDialog(null,"Ati introdus date gresite!\n Daca nu aveti cont va rugam contactati administratorul!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

